    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed

        if (jTextFieldNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, informe o nome do cliente\n");
            jTextFieldNome.requestFocus();
        } else if (jTextFieldEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, informe o email\n");
            jTextFieldEmail.requestFocus();
        } else {

            try {

                cliente.setId(clienteAnterior.getId());
                cliente.setNome(jTextFieldNome.getText().trim());
                cliente.setEmail(jTextFieldEmail.getText().trim());
                
                if (cliente.getId() == 0) {
                    controle.incluirCliente(cliente);
                    System.out.println(cliente.toString());
                } else {
                    int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente alterar este registro ? ");
                    if (opcao == 0){
                        controle.alterarCliente(cliente);
                    }
                }
                clienteAnterior.setId(0);
                limpaCampos();
                imprimirDadosClientes(controle.recuperar());
                desabilitaCampos();

            } catch (Exception erro) {
                JOptionPane.showMessageDialog(null, erro.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

