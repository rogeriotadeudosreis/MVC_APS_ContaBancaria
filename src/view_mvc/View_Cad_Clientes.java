    public void imprimirDadosClientes(ArrayList<ClienteModel> lista) throws Exception {
        DefaultTableModel model = (DefaultTableModel) jTableCadClientes.getModel();
        model.setNumRows(0);
        Collections.sort(lista);
        for (int pos = 0; pos < lista.size(); pos++) {
            String[] linha = new String[3];
            ClienteModel aux = lista.get(pos);
            linha[0] = "" + aux.getId();
            linha[1] = aux.getNome().trim().toUpperCase();
            linha[2] = aux.getEmail();
            model.addRow(linha);
        }
    }

    public void preencherFormulario() throws Exception {
        int linha = jTableCadClientes.getSelectedRow();
        if (linha >= 0) {
            String campoId = jTableCadClientes.getValueAt(linha, 0).toString();
            String campoNome = jTableCadClientes.getValueAt(linha, 1).toString();
            String campoEmail = jTableCadClientes.getValueAt(linha, 2).toString();
            jTextFieldNome.setText(campoNome);
            jTextFieldEmail.setText(campoEmail);
            jTextFieldNome.requestFocus();
            
            clienteAnterior.setId(Integer.parseInt(campoId));
        }
    }

    public void habilitaCampos() {
        jTextFieldNome.setEnabled(true);
        jTextFieldEmail.setEnabled(true);
    }

    public void desabilitaCampos() {
        jTextFieldNome.setEnabled(false);
        jTextFieldEmail.setEnabled(false);
    }

